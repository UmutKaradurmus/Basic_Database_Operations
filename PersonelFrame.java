    private void tabloMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabloMousePressed
        try {
            int satır = tablo.getSelectedRow();//Hangi satır olduğunu alıyor.
            MsSqlConnection msql = new MsSqlConnection();
            int b = 0;
            Statement stmt = msql.stmtver();
            String sql = "select * from Personel ";
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                if (b == satır) {

                    String id = rs.getString("id");
                    String adi = rs.getString("adi");
                    String soyadi = rs.getString("soyadi");
                    String maaşi = rs.getString("maaş");
                    String şehiri = rs.getString("ySehir");
                    ID.setText(id);
                    ad.setText(adi);
                    Soyad.setText(soyadi);
                    maaş.setText(maaşi);
                    ySehir.setText(şehiri);
                    Aranan.setText("");
                    String dt = rs.getString("dogTah");

                    String yılı = dt.substring(0, 4);
                    String ayı = dt.substring(5, 7);
                    String günü = dt.substring(9, 10);

                    yıl.setText(yılı);
                    int i = Integer.parseInt(ayı);
                    int j = Integer.parseInt(günü);
                    gün.setSelectedIndex(j - 1);
                    ay.setSelectedIndex(i - 1);

                    b++;
                }
                b++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabloMousePressed

