    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        PersonelFrame frame = new PersonelFrame();
        MsSqlConnection msql = new MsSqlConnection();

        String geçici = ID.getText();
        if (ID.getText().equals("")) {
            JOptionPane.showMessageDialog(frame, "Kişi silmek için lütfen id giriniz");
        } else {
            int id = Integer.parseInt(geçici);
            final JLabel label = new JLabel();
            int result = JOptionPane.showConfirmDialog(frame, "Silmek İstediğinize emin misiniz?", "Kayıt silme",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                try {
                    msql.delete(id, msql.stmtver());
                    JOptionPane.showMessageDialog(frame, "Kişi silindi");
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                ID.setText("");
                ad.setText("");
                Soyad.setText("");
                maaş.setText("");
                ySehir.setText("");
                Aranan.setText("");
                yıl.setText("");
                gün.setSelectedIndex(0);
                ay.setSelectedIndex(0);
                scombo.setSelectedIndex(0);
                acombo.setSelectedIndex(0);

            } else if (result == JOptionPane.NO_OPTION) {
                label.setText("Hayırı Seçtin");
            } else {
                label.setText("Seçim yapılmadı");
            }
        }

    }//GEN-LAST:event_silActionPerformed

