    private void güncellemebutonuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_güncellemebutonuActionPerformed
        PersonelFrame frame = new PersonelFrame();
        MsSqlConnection msql = new MsSqlConnection();
        Statement stmt = null;
        int sayaç = 0;
        if (!ad.getText().equals("") || !ySehir.getText().equals("") || !yıl.getText().equals("") || !maaş.getText().equals("") || !Soyad.getText().equals("")) {
            try {
                stmt = msql.stmtver();
            } catch (SQLException ex) {
                Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            String tmp = ID.getText();
            int id = Integer.parseInt(tmp);

            if (!ad.getText().equals("")) {
                String isim = ad.getText();
                String geçici = "Update Personel Set adi='" + isim + "' where id= " + id;
                try {
                    stmt.execute(geçici);
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Güncellendi");

                sayaç++;
            }
            if (!Soyad.getText().equals("")) {
                String soyad = Soyad.getText();
                String geçici = "Update Personel Set soyadi='" + soyad + "' where id= " + id;
                try {
                    stmt.execute(geçici);
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Güncellendi");

                sayaç++;
            }
            if (!maaş.getText().equals("")) {
                String ücret = maaş.getText();
                int i = Integer.parseInt(ücret);
                String geçici = "Update Personel Set maaş='" + i + "' where id= " + id;
                try {
                    stmt.execute(geçici);
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Güncellendi");

                sayaç++;
            }
            if (!yıl.getText().equals("")) {
                String dogTah = "";

                String yılı = yıl.getText();

                dogTah += yılı + "-";
                String ayı = (String) ay.getSelectedItem().toString();
                dogTah += ayı + "-";
                String günü = (String) gün.getSelectedItem().toString();
                dogTah += günü;
                String geçici = "Update Personel Set dogTah='" + dogTah + "' where id= " + id;
                try {
                    stmt.execute(geçici);
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Güncellendi");
                sayaç++;
            }
            if (!ySehir.getText().equals("")) {
                String sehir = ySehir.getText();
                String geçici = "Update Personel Set ySehir='" + sehir + "' where id= " + id;
                try {
                    stmt.execute(geçici);
                } catch (SQLException ex) {
                    Logger.getLogger(PersonelFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println("Güncellendi");
            } else {
                JOptionPane.showMessageDialog(frame, "Güncellemek için güncellemek istediğiniz alanın ve id nin dolu olduğundan emin olunuz ");
            }
            if (sayaç > 0) {
                JOptionPane.showMessageDialog(frame, "Kişi güncellendi ");
            } else {
                JOptionPane.showMessageDialog(frame, "Güncellemek için güncellemek istediğiniz alanın ve id nin dolu olduğundan emin olunuz ");
            }
            sayaç = 0;
            ID.setText("");

            ad.setText("");
            Soyad.setText("");
            maaş.setText("");
            ySehir.setText("");
            Aranan.setText("");
            yıl.setText("");
            gün.setSelectedIndex(0);
            ay.setSelectedIndex(0);
            scombo.setSelectedIndex(0);
            acombo.setSelectedIndex(0);
            PersonelFrame f = new PersonelFrame();
        } else {
            PersonelFrame f = new PersonelFrame();
            JOptionPane.showMessageDialog(frame, "Lütfen Kişi Bilgilerini güncellemek için id ve güncellenecek alanın dolu olduğundan emin olunuz");
        }
    }//GEN-LAST:event_güncellemebutonuActionPerformed

